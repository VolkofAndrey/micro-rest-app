export const getActivityById = (id: string): Activity | undefined => {
  return ACTIVITIES.find(a => a.id === id);
};

export const getActivitiesForContext = (emotion: Emotion, location: LocationType): Activity[] => {
  return ACTIVITIES.filter(activity => {
    // SOS activities are only for SOS mode
    if (activity.isSOS) return false;
    
    const matchesEmotion = activity.tags.emotions.includes(emotion);
    const matchesLocation = activity.tags.locations.includes(location);
    
    return matchesEmotion && matchesLocation;
  });
};

export const getActivitiesByCategory = (category: ActivityCategory): Activity[] => {
  return ACTIVITIES.filter(a => a.category === category && !a.isSOS);
};

export const checkAchievements = (history: HistoryEntry[], streak: number): string[] => {
  const unlocked: string[] = [];
  
  // First Step
  if (history.length >= 1) unlocked.push('first_step');
  
  // Streak-based
  if (streak >= 7) unlocked.push('week_warrior');
  if (streak >= 30) unlocked.push('month_champion');
  
  // Category-based
  const breathCount = history.filter(e => getActivityById(e.activityId)?.category === 'BREATHING').length;
  if (breathCount >= 10) unlocked.push('breath_master');
  
  const audioCount = history.filter(e => getActivityById(e.activityId)?.category === 'AUDIO').length;
  if (audioCount >= 5) unlocked.push('audio_lover');
  
  const uniqueCategories = new Set(history.map(e => getActivityById(e.activityId)?.category)).size;
  if (uniqueCategories >= 6) unlocked.push('variety_seeker');
  
  return unlocked;
};